public class Typecasting {
    public static void main(String[] args) {

        int mynum = 9;
        double Mynum = mynum;
        float myNum = mynum;
        System.out.println(Mynum);
        System.out.println(myNum);

        double mYnum = 9.6d;
        int mynUm = (int) mYnum;
        System.out.println(mynUm);
    }

}
